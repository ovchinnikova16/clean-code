using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Markdown
{
    class TagsPair
    {
        public Tag Opening { get; set; } 
        public Tag Closing { get; set; }

        public TagsPair(Tag openingTag, Tag closingTag)
        {
            Opening = openingTag;
            Closing = closingTag;
        }
    }
}
